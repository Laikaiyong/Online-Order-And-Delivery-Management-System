    private void userTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTableMouseClicked
        try
        {
            int selectedRow = userTable.getSelectedRow();
            if (selectedRow >= 0)
            {
                String selectedOrderId = String.valueOf(
                    userTable.getModel().getValueAt(selectedRow, 0)
                );
                for(Admin admin: users.admins)
                {
                    if(admin.getPersonalId().equals(selectedOrderId))
                    {
                        manipulateForm(selectedRow);
                        break;
                    }
                }
                
                for(DeliveryStaff staff: users.deliveryStaff)
                {
                    if(staff.getPersonalId().equals(selectedOrderId))
                    {
                        manipulateForm(selectedRow);
                        break;
                    }
                }
            }
        }
        catch(NumberFormatException excep)
        {
            System.err.println("No row being selected");
        }
    }//GEN-LAST:event_userTableMouseClicked

     private void filterTable(String roleName)
    {
         // Table
        DefaultTableModel tableModel =  (DefaultTableModel) userTable.getModel();
        tableModel.setRowCount(0);
        try {
            if (roleName.equals("Admin"))
            {
                for (Admin admin: admins)
                    {
                        tableModel.addRow(
                            new Object[]
                                {
                                    admin.getPersonalId(),
                                    admin.getUsername(),
                                    admin.getGender(),
                                    ""
                                }
                        );
                    }
            } else {
                for (DeliveryStaff deliverystaff: staff)
                {
                    tableModel.addRow(
                        new Object[]
                            {
                                deliverystaff.getPersonalId(),
                                deliverystaff.getUsername(),
                                deliverystaff.getGender(),
                                deliverystaff.getCarPlate()
                            }
                    );
                }
            }
        } catch (Exception e) {
            System.err.println(e);
        }
    }
    
    
    private String searchRecord()
    {
        String searchPhrase = searchField.getText();
        searchTable(searchPhrase);
        for (Admin admin: admins)
        {
            if (admin.getUsername().equals(searchPhrase))
            {
                setupComponent(admin);
                return "Admin found";
            };
        }
        
        for (DeliveryStaff deliverystaff: staff)
        {
            if (deliverystaff.getUsername().equals(searchPhrase))
            {
                setupComponent(deliverystaff);
                return "Delivery staff found";
            }
        }
        return "";
    }
    
    private void manipulateForm(int recordNo)
    {
        if (recordNo > users.admins.size() - 1)
        {
            DeliveryStaff staff = users.deliveryStaff.get(recordNo - users.admins.size());
            setupComponent(staff);
        }
        else
        {
            Admin admin = users.admins.get(recordNo);
            setupComponent(admin);
        }
    }
    
    private void setupComponent(DeliveryStaff staff)
    {
        idText.setText(staff.getPersonalId());
        usernameField.setText(staff.getUsername());
        carPlateField.setText(staff.getCarPlate());
        switch (staff.getGender()) {
            case 'M':
                maleButton.getModel().setSelected(true);
                break;
            case 'F':
                femaleButton.getModel().setSelected(true);
                break;
            default:
                break;
        }
        roleDisplay.setText("Delivery Staff");
    }
    
    private void setupComponent(Admin admin)
    {
        ArrayList<String> roleName = new ArrayList<>();
        roleName.add("Default");
        roleName.add("Admin");
        roleName.add("Delivery Staff");
        
        roleCombo.removeAllItems();
        roleCombo.setModel(new DefaultComboBoxModel<>(roleName.toArray(String[]::new)));
        
        roleName.remove("Default");
        
        createRoleCombo.removeAllItems();
        createRoleCombo.setModel(new DefaultComboBoxModel<>(roleName.toArray(String[]::new)));
        
        idText.setText(admin.getPersonalId());
        usernameField.setText(admin.getUsername());
        carPlateField.setText("");
        switch (admin.getGender()) {
            case 'M':
                maleButton.getModel().setSelected(true);
                break;
            case 'F':
                femaleButton.getModel().setSelected(true);
                break;
            default:
                break;
        }
        roleDisplay.setText("Admin");
    }
    
