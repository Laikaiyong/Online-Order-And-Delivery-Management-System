    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        String id = idText.getText();
        boolean proceedable = true;
         for(Admin admin: users.admins)
         {
             if(admin.getPersonalId().equals(id))
             {
                 int adminIndex = users.admins.indexOf(admin);
                 admin.setUsername(usernameField.getText());
                 admin.setGender(maleButton.isSelected() ? 'M' : 'F');
                 
                 users.admins.set(adminIndex, admin);
                 proceedable = false;
                 new GeneraFileHandler().updateUserFile(users);
                 new PopUp().successMessage("Success admin update", "Modify successfully");
                 dispose();
                 new UserManagement().setVisible(true);
             }
         }
         
         if (proceedable)
         {
             for (DeliveryStaff staff: users.deliveryStaff)
             {
                if(staff.getPersonalId().equals(id))
                {
                    int staffIndex = users.deliveryStaff.indexOf(staff);
                    staff.setUsername(usernameField.getText());
                    staff.setGender(maleButton.isSelected() ? 'M' : 'F');
                    staff.setCarPlate(carPlateField.getText());

                    users.deliveryStaff.set(staffIndex, staff);
                    proceedable = false;
                    new GeneraFileHandler().updateUserFile(users);
                    new PopUp().successMessage("Success delivery staff update", "Modify successfully");
                    dispose();
                    new UserManagement().setVisible(true);
                }
             }
         }
    }//GEN-LAST:event_updateButtonActionPerformed

